number1 = 10
number2 = 5 

# Addition
sum_result = number1 + number2
print("Addition of", number1, "and", number2, "is", sum_result)

# Subtraction
diff_result = number1 - number2
print("Subtraction of", number1, "and", number2, "is", diff_result)

# Multiplication
prod_result = number1 * number2 
print("Multiplication of", number1, "and", number2, "is", prod_result)
